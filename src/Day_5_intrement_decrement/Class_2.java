package Day_5_intrement_decrement;

public class Class_2 {
    public static void main(String[] args) {

        String a = "java";
        String b = "is funny";
        System.out.println(a+b);
        System.out.println(a+ " "+b);

        int c = 10;
        int d = 20;

        System.out.println(a + c + d);
        System.out.println(c+d+a);
        System.out.println(a+ (c+d));

        String g = "Hello";
        String h = "World";
        System.out.println(g+h);
        System.out.println(g+" "+h);

        System.out.println(a+ c*d );
    }
}
