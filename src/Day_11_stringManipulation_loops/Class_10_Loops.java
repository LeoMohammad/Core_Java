package Day_11_stringManipulation_loops;

public class Class_10_Loops {
    public static void main(String[] args) {

        for (int i = 0; i<10; i++) {
            System.out.println(i);
        }


        System.out.println("---------------");

        for (int i = 10; i > 0 ; i++) { // infinite loop
            System.out.print(i +" ");
        }

    }
}
