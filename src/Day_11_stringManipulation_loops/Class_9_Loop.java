package Day_11_stringManipulation_loops;

public class Class_9_Loop {

    public static void main(String[] args) {

        for (int i = 0; i <10; i++) {
            System.out.print(i);
        }

        System.out.println();



        for (int i =10; i>=0; i--) {

            System.out.print(i);


        }











        }



    }

