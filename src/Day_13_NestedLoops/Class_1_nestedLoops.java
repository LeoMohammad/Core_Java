package Day_13_NestedLoops;

public class Class_1_nestedLoops {

    /*
     * 		     * * * * * * * *		* * * * * * * *
     * *		     * * * * * * * *		* * * * * *
     * * *		 * * * * * * * * 		* * * *
     * * * *		 * * * * * * * *        * *
     */

    public static void main(String[] args) {

        for (int j = 1; j <=4 ; j++) {


            for (int i = 1; i <=8 ; i++) {
                System.out.print("* ");
            }

            System.out.println();

        }

    }


}
