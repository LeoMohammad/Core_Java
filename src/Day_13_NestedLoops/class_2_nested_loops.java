package Day_13_NestedLoops;

public class class_2_nested_loops {

    /*
     *
     * *
     * * *
     * * * *
     */

    public static void main(String[] args) {

        for (int j = 1; j <= 4; j++) {

            for (int i = 1; i <= j; i++) {
                System.out.print("* ");
            }
            System.out.println();
        }




    }


}
